n = 1
soma = 0
while (n <= 10):
    if n == 1:
        num = int(input("Type the 1st number: "))
    elif n == 2:
        num = int(input("Type the 2nd number: "))
    elif n == 3:
        num = int(input("Type the 3rd number: "))
    else:
        num = int(input("Type the {}th number: ".format(n)))
    n += 1
    soma = soma + num
print ("The average is", soma/(n-1))


        
